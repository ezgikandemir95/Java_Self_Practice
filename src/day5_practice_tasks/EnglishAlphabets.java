package day5_practice_tasks;

public class EnglishAlphabets {
    public static void main(String[] args) {


        for (int j = 0; j < 1 ; j++) {
            for (char i = 'A'; i <= 'Z'; i++) {
                System.out.print(i + " ");
            }
            System.out.println();
        }


    }
}




